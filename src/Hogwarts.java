public class Hogwarts {
    private String name;
    private int distance;
    private int magic;

    public Hogwarts(String name, int distance, int magic){
        this.name = name;
        this.distance =distance;
        this.magic =magic;

    }

    public String getName() {
        return name;
    }

    public int getDistance() {
        return distance;
    }

    public int getMagic() {
        return magic;
    }
}
